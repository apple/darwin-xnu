char *
strncat(char *s1, const char *s2, unsigned long n)
{
	char *os1;
	int i = n;

	os1 = s1;
	while (*s1++)
		;
	--s1;
	while ((*s1++ = *s2++))
		if (--i < 0) {
			*--s1 = '\0';
			break;
		}
	return(os1);
}
